package com.tnsif.day3;

public class ForEachLoop {

	public static void main(String[] args) {
		// for each loop demo
		int a[]= {10,20,30,40,50};
//		for integer array
		for(int i:a)
		{
			System.out.println(i);
		}
		char ch[]= {'g','a','n','g','a'};
		for(char c:ch)
		{
			System.out.println(c);
		}
		float f[]= {10,20,30,40,50};
		for(float f1:f)
		{
			System.out.println(f1);
		}
	}
}
